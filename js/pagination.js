let currentPage = 1;
let itemsPerPage = 10;

function render() {
    tableBodyElement.innerHTML = "";
    let indexOfLastPage = currentPage * itemsPerPage;
    let indexOfFirstPage = indexOfLastPage - itemsPerPage;
    let currentItems = data.slice(indexOfFirstPage, indexOfLastPage);
    //console.log(currentItems)

    //loop data in row and table cell
    currentItems.forEach(element => {
        const tableRowElement = (createElement("tr", {
            class:'text-center'
        }))
        const idElement = (createElement("td"))
        idElement.innerText = element.id
        const nameElement = (createElement("td"))
        nameElement.innerText = element.name
        const emailElement = (createElement("td"))
        emailElement.innerText = element.email
        //append
        tableBodyElement.append(tableRowElement)
        tableRowElement.append(idElement, nameElement, emailElement)
    });
    
}
render();


let paginationContElement = document.createElement('div')
paginationContElement.setAttribute('class', 'd-flex justify-content-center gap-2')
paginationContElement.setAttribute('id', 'buttons')
document.body.appendChild(paginationContElement);

function pagination() {
    const datalength = Math.ceil(data.length / itemsPerPage);
    let prevElement = document.createElement('button')
    prevElement.setAttribute('class', 'btn');
    prevElement.setAttribute('id', 'prevBtn')
    prevElement.innerHTML = "Prev";
    paginationContElement.appendChild(prevElement);
    prevElement.addEventListener('click', () => {        
        currentPage = currentPage - 1;
        if (currentPage < 1) currentPage = 1;
        render();
    })
    for (let i = 1; i <= datalength; i++) {
        let buttonElement = document.createElement('button')
        buttonElement.setAttribute('class', 'btn')
        buttonElement.setAttribute('id', `buttons${i}`)
        buttonElement.innerHTML = i
        paginationContElement.appendChild(buttonElement);
        buttonElement.addEventListener('click', () => {
            currentPage = buttonElement.innerHTML;
            // console.log(currentPage)
            render();
        })
    }
    let nextElement = document.createElement('button')
    nextElement.setAttribute('class', 'btn');
    nextElement.setAttribute('id', 'nextBtn')
    nextElement.innerHTML = "Next";
    paginationContElement.appendChild(nextElement);
    nextElement.addEventListener('click', () => {
        currentPage = currentPage + 1;
        if (currentPage > datalength) currentPage = datalength;
        render();
    })
    //first page active class
    const active = document.getElementById("buttons1");
    active.classList.add("active");
    //dynamic active class for pagination
    const btnActive = document.querySelectorAll('.btn');
    btnActive.forEach((btn) => {
        btn.addEventListener('click', () => {
            document.querySelector('.active')?.classList.remove('active')
            btn.classList.add('active');
        })
    })
    
}
pagination();